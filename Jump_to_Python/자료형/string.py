# 문자열: " " or ' ' or """ """ or ''' '''
a = "Hello, world!"
print(a)
print(type(a))
b = "a"
print(b)
print(type(b))
c = "123"
print(c)
print(type(c))

# escape code: \n, \t, \\, \', \"
print("Hello \nworld")
print("""hello 
world""")
print("Hello\tworld")
print("Hello\\world")
print("Hello\'world'")